package com.practice.java8.interfaces;

public class Persona {

    private int id;
    private String name;

    public Persona() {
    }

    public Persona(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
